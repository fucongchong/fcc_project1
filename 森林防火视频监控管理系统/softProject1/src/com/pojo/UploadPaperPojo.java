package com.pojo;

public class UploadPaperPojo {
private String TopicID;
private String PID;
private String PN;
private String author;
private String provenance;
private String keyword;
private String digest;
private String filePhysicalLocation;
private String SNo;
private String uploadTime;
private String degOfCorrelation;
public String getTopicID() {
	return TopicID;
}
public void setTopicID(String topicID) {
	TopicID = topicID;
}
public String getPID() {
	return PID;
}
public void setPID(String pID) {
	PID = pID;
}
public String getPN() {
	return PN;
}
public void setPN(String pN) {
	PN = pN;
}
public String getAuthor() {
	return author;
}
public void setAuthor(String author) {
	this.author = author;
}
public String getProvenance() {
	return provenance;
}
public void setProvenance(String provenance) {
	this.provenance = provenance;
}
public String getKeyword() {
	return keyword;
}
public void setKeyword(String keyword) {
	this.keyword = keyword;
}
public String getDigest() {
	return digest;
}
public void setDigest(String digest) {
	this.digest = digest;
}
public String getFilePhysicalLocation() {
	return filePhysicalLocation;
}
public void setFilePhysicalLocation(String filePhysicalLocation) {
	this.filePhysicalLocation = filePhysicalLocation;
}
public String getSNo() {
	return SNo;
}
public void setSNo(String sNo) {
	SNo = sNo;
}
public String getUploadTime() {
	return uploadTime;
}
public void setUploadTime(String uploadTime) {
	this.uploadTime = uploadTime;
}
public String getDegOfCorrelation() {
	return degOfCorrelation;
}
public void setDegOfCorrelation(String degOfCorrelation) {
	this.degOfCorrelation = degOfCorrelation;
}

}
